    private static Size chooseVideoSize(Size[] choices) {
        for (Size size : choices) {
            if (size.getWidth() == size.getHeight() * 4 / 3 && size.getWidth() <= 1080) {
                return size;
            }
        }
        Log.e(TAG, "Couldn't find any suitable video size");
        return choices[choices.length - 1];
    }

    private static Size chooseOptimalSize(Size[] choices, int width, int height, Size aspectRatio) {
        List<Size> bigEnough = new ArrayList<Size>();
        int w = aspectRatio.getWidth();
        int h = aspectRatio.getHeight();
        for (Size option : choices) {
            if (option.getHeight() == option.getWidth() * h / w &&
                    option.getWidth() >= width && option.getHeight() >= height) {
                bigEnough.add(option);
            }
        }

        if (bigEnough.size() > 0) {
            return Collections.min(bigEnough, new CompareSizesByArea());
        } else {
            Log.e(TAG, "Couldn't find any suitable preview size");
            return choices[0];
        }
    }

    private void loadFfmpeg() {

        FFmpeg ffmpeg = FFmpeg.getInstance(getActivity().getApplicationContext());
        try {
            ffmpeg.loadBinary(new LoadBinaryResponseHandler() {

                @Override
                public void onStart() {
                }

                @Override
                public void onFailure() {
                }

                @Override
                public void onSuccess() {
                }

                @Override
                public void onFinish() {
                }
            });
        } catch (FFmpegNotSupportedException e) {
        }
    }

    private void cropVideo() {
        int x = mVideoSize.getHeight();

        String command = "-i " + mNextVideoAbsolutePath + " -vf crop=" + x + ":" + x + ":0:0 " + mCropVideoFileName;
        final String[] cmd = command.split(" ");
        FFmpeg ffmpeg = FFmpeg.getInstance(getActivity().getApplicationContext());
        try {
            // to execute "ffmpeg -version" command you just need to pass "-version"
            ffmpeg.execute(cmd, new ExecuteBinaryResponseHandler() {

                @Override
                public void onStart() {
                    mButtonVideo.setClickable(false);
                    Toast.makeText(getActivity().getApplicationContext(), "Please wait your video is getting processed", Toast.LENGTH_SHORT).show();

                }


                @Override
                public void onProgress(String message) {
                    System.out.println(message);


                }

                @Override
                public void onFailure(String message) {
                    mButtonVideo.setClickable(true);
                    Toast.makeText(getActivity().getApplicationContext(), "Try again. Cropping failed.", Toast.LENGTH_SHORT).show();

                }

                @Override
                public void onSuccess(String message) {
                    Intent intent = new Intent(getActivity().getApplicationContext(), VideoPlayer.class);
                    startActivity(intent);
                }

                @Override
                public void onFinish() {
                }
            });
        } catch (FFmpegCommandAlreadyRunningException e) {
            // Handle if FFmpeg is already running
        }
    }


